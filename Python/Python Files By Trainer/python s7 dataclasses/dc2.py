from dataclasses import dataclass, field
@dataclass(order=True)
#----------------------------------------------------------------------------
class Employee:
#----------------------------------------------------------------------------
    ename: str
    eage : int
    esalary : float = field(repr=False, compare=False)
    epercentage: float = 0
#----------------------------------------------------------------------------
    def displayPercentage(self):
        """..display the employee percentage.."""
        return self.epercentage
#----------------------------------------------------------------------------
emp1 = Employee("anil",24,10000.50,85.36)
print(emp1)
