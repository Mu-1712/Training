##----------------------------------------------------------------------------
##				numpy.empty
##----------------------------------------------------------------------------
##It creates an uninitialized array of specified shape and dtype. 
##----------------------------------------------------------------------------
##numpy.empty(shape, dtype = float, order = 'C')
##----------------------------------------------------------------------------
##Sr.No.	Parameter  			Description
##----------------------------------------------------------------------------
##1	Shape		Shape of an empty array in int or tuple of int
##----------------------------------------------------------------------------
##2	Dtype		Desired output data type. 
##----------------------------------------------------------------------------			
##			Optional
##----------------------------------------------------------------------------
##3	Order		'C' for C-style row-major array, 
##----------------------------------------------------------------------------			
##			'F' for FORTRAN style column-major array
##----------------------------------------------------------------------------
import numpy as np 
##--------------------------------------------------------------------------
x = np.empty([3,2], dtype = int) 
print (x)
##--------------------------------------------------------------------------
##output
##[[        76          0]
## [-978927536      32767]
## [-979048944      32767]]
