##----------------------------------------------------------------------------
##Each built-in data type has a character code that uniquely identifies it.
##----------------------------------------------------------------------------
##	'b' - boolean
##----------------------------------------------------------------------------
##	'i' - (signed) integer
##----------------------------------------------------------------------------
##	'u' - unsigned integer
##----------------------------------------------------------------------------
##	'f' - floating-point
##----------------------------------------------------------------------------
##	'c' - complex-floating point
##----------------------------------------------------------------------------
##	'm' - timedelta
##----------------------------------------------------------------------------
##	'M' - datetime
##----------------------------------------------------------------------------
##	'O' - (Python) objects
##----------------------------------------------------------------------------
##	'S', 'a' - (byte-)string
##----------------------------------------------------------------------------
##	'U' - Unicode
##----------------------------------------------------------------------------
##	'V' - raw data (void)
##----------------------------------------------------------------------------
