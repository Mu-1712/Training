##--------------------------------------------------------------------------
# dtype parameter
##--------------------------------------------------------------------------
# desired data type of array, optional
##--------------------------------------------------------------------------
import numpy as np 
##--------------------------------------------------------------------------
a = np.array([1, 2, 3], dtype = complex) 
print (a)
##--------------------------------------------------------------------------
##output
##[1.+0.j 2.+0.j 3.+0.j]
##--------------------------------------------------------------------------
a = np.array([1, 2, 3], dtype = int) 
print (a)
##--------------------------------------------------------------------------
##output
##[1 2 3]
##--------------------------------------------------------------------------
a = np.array([1, 2, 3], dtype = float) 
print (a)
##--------------------------------------------------------------------------
##output
##[1. 2. 3.]
##--------------------------------------------------------------------------
a = np.array([1, 2, 3], dtype = long) 
print (a)
