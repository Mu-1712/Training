##The basic ndarray is created using an array function in NumPy
##----------------------------------------------------------------------------
##			numpy.array
##----------------------------------------------------------------------------			
##It creates an ndarray from any object exposing array interface, or 
##from any method that returns an array.
##----------------------------------------------------------------------------
##numpy.array(object, dtype = None, copy = True, order = None, subok = False, 
##		ndmin = 0)
##----------------------------------------------------------------------------
##The above constructor takes the following parameters :
##----------------------------------------------------------------------------
##Sr.No.	Parameter  			Description
##----------------------------------------------------------------------------
##1	object		Any object exposing the array interface method 
##			returns an array, or any (nested) sequence.
##----------------------------------------------------------------------------
##2	dtype		Desired data type of array, optional
##----------------------------------------------------------------------------
##3	copy		Optional. By default (true), the object is copied
##----------------------------------------------------------------------------
##4	order		C (row major) or F (column major) or A (any) (default)
##----------------------------------------------------------------------------
##5	subok		By default, returned array forced to be a base
##                        class array. 
##			If true, sub-classes passed through
##----------------------------------------------------------------------------
##6	ndmin		Specifies minimum dimensions of resultant array
##----------------------------------------------------------------------------
##
##
