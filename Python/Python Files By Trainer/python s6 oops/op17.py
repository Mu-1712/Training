#--------------------------------------------------------------------------
# private attribute encapsulation
#--------------------------------------------------------------------------
class Book:
#--------------------------------------------------------------------------
    __count=0   # private attribute
#--------------------------------------------------------------------------
    def __init__(self):
        Book.__count = Book.__count +1
#--------------------------------------------------------------------------
    def displayCount(self):
        print(Book.__count)
#--------------------------------------------------------------------------
b1=Book()
b1.displayCount()
b2=Book()
b2.displayCount()
b3=Book()
b3.displayCount()
Book.__count=9      
print(Book.__count)
