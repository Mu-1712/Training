#--------------------------------------------------------------------------
# multi level inheritance
#--------------------------------------------------------------------------
class Mathematics:
#--------------------------------------------------------------------------
    def m(self):
        print("maths")
#--------------------------------------------------------------------------
class Statistics(Mathematics):  # child of Mathematics
#--------------------------------------------------------------------------
    def s(self):
        print("stats")
#--------------------------------------------------------------------------
class Analytics(Statistics):     # child of Statistics
#--------------------------------------------------------------------------
    def a(self):
        print("analytics")
#--------------------------------------------------------------------------
x=Analytics()
x.a()
x.s()
x.m()
