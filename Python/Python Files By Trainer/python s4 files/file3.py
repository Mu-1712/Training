# Open a file
#--------------------------------------------------------------------------
#	file_object = open(file_name [, access_mode][, buffering])
#--------------------------------------------------------------------------
#		access modes = r,w,b,rb,wb,r+,w+,rb+,wb+,a,ab,a+,ab+
