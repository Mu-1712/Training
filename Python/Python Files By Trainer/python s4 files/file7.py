#--------------------------------------------------------------------------
# Open a file
#--------------------------------------------------------------------------
a = open("xyz.txt", "r+")
str = a.read(10);
print ("Read String is : ", str)
#--------------------------------------------------------------------------
# Check current position
#--------------------------------------------------------------------------
position = a.tell();
print ("Current file position : ", position)
#--------------------------------------------------------------------------
# Reposition pointer at the beginning once again
#--------------------------------------------------------------------------
position = a.seek(0, 0);
str = a.read(10);
print ("Again read String is : ", str)
#--------------------------------------------------------------------------
# Close opend file
a.close()
