---------------------------------------------------------------------------
--	In SQL Server, a synonym is an alias or alternative name for a 
--	database object such as a table, view, 
--	stored procedure, user-defined function and sequence
---------------------------------------------------------------------------
use db1;
go
---------------------------------------------------------------------------
create table s1.emp5(	empid int primary key,
			empname varchar(10) not null,
			empage int );
go
---------------------------------------------------------------------------
create synonym e1 for db1.s1.emp5;
go
---------------------------------------------------------------------------
-- db1.s1.emp5 	<--  	this is a fully qualified sql server object
---------------------------------------------------------------------------
-- all the synonyms  are stored in default schema : dbo
---------------------------------------------------------------------------
-- check -> db1 -> synonyms 
---------------------------------------------------------------------------
insert into e1 values(101,'sunil',24);
go
---------------------------------------------------------------------------
insert into e1 values(102,'suresh',24);
go
---------------------------------------------------------------------------
insert into e1 values(103,'sanjay',28);
go
---------------------------------------------------------------------------
select * from e1;
go
---------------------------------------------------------------------------
drop synonym e1;
go
