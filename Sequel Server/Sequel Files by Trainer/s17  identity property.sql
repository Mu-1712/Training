use db1;
go
-----------------------------------------------------------------------------
--	use of the identity property
--	identity[(seed,increment)]
--	the seed is the value of the first row loaded into the table
--	the increment is the incremental value added to the identity value 
--	of the previous row
-----------------------------------------------------------------------------
create table s1.emp2(	empid int identity(200,5) primary key, 
			empname varchar(10) not null, 
			empage int );
go
