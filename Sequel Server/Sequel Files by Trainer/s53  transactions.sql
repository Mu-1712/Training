transactions must follow : acid properties : 
-----------------------------------------------------------------------------
atomicity : consistency : isolation : durability
-----------------------------------------------------------------------------
 3 different transactions modes :
-----------------------------------------------------------------------------
		1.	Autocommit :	
-----------------------------------------------------------------------------
The successful statements are committed and the failed statements are rolled back immediately
-----------------------------------------------------------------------------
		2.	Implicit   :	
-----------------------------------------------------------------------------
Enables to start an implicit transaction for every DML statement but 
we need to use the commit or rolled back commands explicitly at the 
end of the statements
-----------------------------------------------------------------------------			SET IMPLICIT_TRANSACTIONS ON 
-----------------------------------------------------------------------------
		3.	Explicit   :	
-----------------------------------------------------------------------------
It provides to define a transaction exactly with the starting and 
ending points of the transaction
